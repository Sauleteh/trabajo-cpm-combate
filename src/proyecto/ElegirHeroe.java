    private void BEjecutarCuadriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEjecutarCuadriculaActionPerformed
        // TODO add your handling code here:
        
        // Parámetros de la tarea
        int tamCuadri = Integer.parseInt(TFCuadricula.getText());
        TareaC.setTamCuadricula(tamCuadri); 
        float SegsIter = Float.parseFloat(CBCuadricula.getSelectedItem().toString());
        TareaC.setSegsInte(SegsIter);
        int anc = PDibujarH.getWidth();
        TareaC.setAncho(anc);
        int alt = PDibujarH.getHeight();
        TareaC.setAlto(alt);
        
        // Crear un hilo que lance la tarea
        HiloC=new Thread(){public void run(){ElegirHeroe.TareaC.Run();}};        
        HiloC.start(); // Lanzar el hilo (ejecutará Run)
        
        // Permitir que desde el interfaz se pueda pausar y parar
        BPararCuadricula.setEnabled(true);
        BEjecutarCuadricula.setEnabled(false);    
        TFCuadricula.setEnabled(false);
        
    }//GEN-LAST:event_BEjecutarCuadriculaActionPerformed

