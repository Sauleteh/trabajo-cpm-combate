    private void BAgregarHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAgregarHActionPerformed
        // TODO add your handling code here:
        if (DPersCustom.showDialog())
        {
            javax.swing.DefaultListModel Model=(javax.swing.DefaultListModel)this.LPersonalizadoH.getModel();
            
            int ind = Model.size();
            heroesPersonalizados[ind] = new Personaje(DPersCustom.getNombreD(), DPersCustom.getVidaD(),
                                DPersCustom.getAtaqueD(), DPersCustom.getDefensaD(),
                                DPersCustom.getProbCritD(), DPersCustom.getFatigaID());
            
            Model.addElement(heroesPersonalizados[ind].getNombre());
            VPrincipal.setHeroe(heroesPersonalizados[ind]);
            VPrincipal.seCambioPersonaje();
            this.LPersonalizadoH.setSelectedIndex(ind);
            RBPersonalizadoH.setSelected(true);
            
            BAgregarH.setEnabled(Model.size() < 5);
            RBPersonalizadoH.setEnabled(true);
        }
    }//GEN-LAST:event_BAgregarHActionPerformed

    private void BEliminarHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEliminarHActionPerformed
        // TODO add your handling code here:
        javax.swing.DefaultListModel Model=(javax.swing.DefaultListModel)this.LPersonalizadoH.getModel();
        int indice = LPersonalizadoH.getSelectedIndex();
        Model.removeElementAt(indice);
        
        if (Model.size() == indice)
        {
            this.LPersonalizadoH.setSelectedIndex(indice - 1);
            
            if (Model.size() == 0)
            {
                VPrincipal.setHeroe("Mago supremo (Pers. oculto)", 999, 99, 99, 99, 0);
            }
            else VPrincipal.setHeroe(heroesPersonalizados[indice-1]);
        }
        else
        {
            this.LPersonalizadoH.setSelectedIndex(indice);
            VPrincipal.setHeroe(heroesPersonalizados[indice]);
        }
        VPrincipal.seCambioPersonaje();
        RBPersonalizadoH.setSelected(true);
    }//GEN-LAST:event_BEliminarHActionPerformed

